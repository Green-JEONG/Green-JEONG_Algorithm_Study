function solution(letter) {
    // 모스부호 딕셔너리
    const morse = { 
        '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
        '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
        '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
        '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
        '-.--':'y','--..':'z'
    };
    
    // 공백 기준으로 모스부호 나누기
    const morseWords = letter.split(' ');
    
    // 각 모스부호 알파벳으로 변환
    const result = morseWords.map(code => morse[code]);
    
    // 변환된 알파벳들을 하나의 문자열로 합치기
    return result.join('');
}